package main

import "fmt"

func main() {
	var name string

	name = "kevin"
	fmt.Println(name)

	name = "ardiansyah"
	fmt.Println(name)

	var lname = "hidayat"
	fmt.Println(lname)

	namaTengah := "ardi"
	fmt.Println(namaTengah)

	//multiple variable
	var (
		firstName = "Kevin"
		lastName  = "Hidayat"
	)
	fmt.Println(firstName)
	fmt.Println(lastName)

}
